def add_numbers(a, b):
    return a + b

def subtract_numbers(a, b):
    return a - b

def multiply_numbers(a, b):
    return a * b

def divide_numbers(a, b):
    return a / b

def square(a):
    return a * a

def pythagorean(a, b):
    return (square(a) + square(b))**0.5

